# Complex Numbers

z = 3 + 4j

print(z)
print(z.real)
print(z.imag)


# Complex Number Addition

a = 2 + 3j
b = 1 + 4j

print(a + b)


# Floating Point Precision

print(0.1 + 0.2)


# Decimal Module Basics

from decimal import Decimal

a = Decimal("0.1")
b = Decimal("0.2")

print(a + b)


# Number Base Conversion

num = 25

print(bin(num))
print(oct(num))
print(hex(num))


# ASCII Value Conversion

print(ord("A"))
print(chr(65))


# Checking Number Type

a = 10
b = 10.5
c = 3 + 2j

print(type(a))
print(type(b))
print(type(c))