# Binary Numbers

num = 10

print(bin(num))


# Binary to Decimal

binary_num = "1010"

decimal_num = int(binary_num, 2)

print(decimal_num)


# Hexadecimal Numbers

num = 255

print(hex(num))


# Hexadecimal to Decimal

hex_num = "FF"

decimal_num = int(hex_num, 16)

print(decimal_num)


# Octal Numbers

num = 20

print(oct(num))


# Octal to Decimal

octal_num = "24"

decimal_num = int(octal_num, 8)

print(decimal_num)